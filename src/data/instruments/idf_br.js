/**
 * Get items for a specific band
 */
export const getItemsByBand = (bandCode) =>
  IDF_BR_ITEMS.filter(item => item.band_code === bandCode);

/**
 * Get intervention mappings for an item + score
 */
export const getInterventions = (itemId, scoreCode) =>
  IDF_BR_INTERVENTION_MAP.filter(m => m.item_id === itemId && m.score_code === scoreCode);

/**
 * Get all interventions for an item (N and P)
 */
export const getAllInterventionsForItem = (itemId) =>
  IDF_BR_INTERVENTION_MAP.filter(m => m.item_id === itemId);

/**
 * Get the appropriate band for a given age in months
 */
export const getBandForAge = (ageMonths) => {
  if (ageMonths <= 12) return 'F1';
  if (ageMonths <= 24) return 'F2';
  // F3 not yet in package
  if (ageMonths <= 48) return 'F4';
  if (ageMonths <= 60) return 'F5';
  return 'F6';
};
