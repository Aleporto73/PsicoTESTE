/**
 * Obtém um domínio pelo seu ID
 * @param {string} domainId - ID do domínio (ex: 'cooperacao_eficacia')
 * @returns {object} Objeto do domínio ou null
 */
export function getABLLSRDomainById(domainId) {
  return ABLLS_R_DOMAINS.find(d => d.id === domainId) || null;
}

/**
 * Obtém um grupo de domínios pelo seu ID
 * @param {string} groupId - ID do grupo
 * @returns {object} Objeto do grupo ou null
 */
export function getABLLSRGroupById(groupId) {
  return ABLLS_R_DOMAIN_GROUPS.find(g => g.id === groupId) || null;
}

/**
 * Calcula scores do ABLLS-R
 * 
 * @param {object} responses - Respostas mapeadas como {domainId_itemNum: score}
 *                             Ex: { cooperacao_eficacia_1: 1, cooperacao_eficacia_2: 0, ... }
 * 
 * @returns {object} Objeto com scores por domínio, total e percentuais
 *   {
 *     domainScores: {
 *       cooperacao_eficacia: {
 *         score: 30,
 *         maxPossible: 47,
 *         items: 19,
 *         percent: 63.83
 *       },
 *       ...
 *     },
 *     groupScores: {
 *       habilidades_basicas: {
 *         score: 150,
 *         maxPossible: 365,
 *         percent: 41.1,
 *         domains: ['cooperacao_eficacia', ...]
 *       },
 *       ...
 *     },
 *     totalScore: 700,
 *     maxPossible: 1485,
 *     totalPercent: 47.13,
 *     itemsAnswered: 500,
 *     totalItems: 543
 *   }
 */
export function calculateABLLSRScores(responses) {
  const domainScores = {};
  let totalScore = 0;
  let itemsAnswered = 0;

  // Calculate domain scores
  for (const domain of ABLLS_R_DOMAINS) {
    let domainScore = 0;
    let domainAnswered = 0;

    for (const item of domain.items) {
      const responseKey = `${domain.id}_${item.num}`;
      if (responses[responseKey] !== undefined) {
        domainAnswered++;
        domainScore += Math.min(responses[responseKey], item.max_pts);
      }
    }

    itemsAnswered += domainAnswered;
    totalScore += domainScore;

    domainScores[domain.id] = {
      score: domainScore,
      maxPossible: domain.max_total,
      items: domain.item_count,
      answered: domainAnswered,
      percent: domain.max_total > 0 ? Math.round((domainScore / domain.max_total) * 100 * 100) / 100 : 0,
    };
  }

  // Calculate group scores
  const groupScores = {};
  for (const group of ABLLS_R_DOMAIN_GROUPS) {
    let groupScore = 0;
    let groupMaxPossible = 0;

    for (const domainId of group.domains) {
      const domainScore = domainScores[domainId];
      if (domainScore) {
        groupScore += domainScore.score;
        groupMaxPossible += domainScore.maxPossible;
      }
    }

    groupScores[group.id] = {
      score: groupScore,
      maxPossible: groupMaxPossible,
      percent: groupMaxPossible > 0 ? Math.round((groupScore / groupMaxPossible) * 100 * 100) / 100 : 0,
      domains: group.domains,
    };
  }

  return {
    domainScores,
    groupScores,
    totalScore,
    maxPossible: ABLLS_R_META.max_score,
    totalPercent: Math.round((totalScore / ABLLS_R_META.max_score) * 100 * 100) / 100,
    itemsAnswered,
    totalItems: ABLLS_R_META.total_items,
  };
}

/**
 * Obtém descrição de nível de desempenho baseado em percentual
 * @param {number} percent - Percentual (0-100)
 * @returns {object} Objeto com level, label e color
 */
export function getABLLSRPerformanceLevel(percent) {
  if (percent >= 80) return { level: 'avancado', label: 'Avançado', color: '#10b981' };
  if (percent >= 60) return { level: 'proficiente', label: 'Proficiente', color: '#3b82f6' };
  if (percent >= 40) return { level: 'em_desenvolvimento', label: 'Em Desenvolvimento', color: '#f59e0b' };
  if (percent >= 20) return { level: 'inicial', label: 'Inicial', color: '#ef5350' };
  return { level: 'nao_demonstrado', label: 'Não Demonstrado', color: '#6b7280' };
}
