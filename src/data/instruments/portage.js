/**
 * Calculate PORTAGE test scores
 * @param {Object} responses - Object with keys like 'socializacao_0_1_01' and values 'sim', 'nao', 'as_vezes', 'na'
 * @returns {Object} Score calculation results with developmental ages
 */
export function calculatePortageScores(responses) {
  const byArea = {};
  let overallMonths = 0;
  let areaCount = 0;

  Object.entries(PORTAGE_AREAS).forEach(([areaId, area]) => {
    byArea[areaId] = {
      byRange: {},
      totalScore: 0,
      totalExpected: 0,
      totalPercent: 0,
      developmentalAge: 0
    };

    let areaMonths = 0;

    PORTAGE_AGE_RANGES.forEach(range => {
      const rangeId = range.id;
      let rangeScore = 0;
      let rangeExpected = 0;

      if (PORTAGE_ITEMS[areaId] && PORTAGE_ITEMS[areaId][rangeId]) {
        Object.entries(PORTAGE_ITEMS[areaId][rangeId]).forEach(([itemNum, item]) => {
          const responseKey = `${areaId}_${rangeId}_${itemNum}`;
          const response = responses[responseKey];

          if (response && response !== 'na') {
            rangeExpected++;
            if (response === 'sim') {
              rangeScore += 1;
            } else if (response === 'as_vezes') {
              rangeScore += 0.5;
            }
          }
        });
      }

      const rangePercent = rangeExpected > 0 ? (rangeScore / rangeExpected) * 100 : 0;
      const expectedCount = PORTAGE_EXPECTED_SCORES[areaId]?.[rangeId] || 0;
      
      // Developmental age contribution: (score / expected) * 12 months for this range
      const rangeContribution = expectedCount > 0 ? (rangeScore / expectedCount) * 12 : 0;
      areaMonths += rangeContribution;

      byArea[areaId].byRange[rangeId] = {
        score: rangeScore,
        expected: expectedCount,
        percent: expectedCount > 0 ? (rangeScore / expectedCount) * 100 : 0
      };

      byArea[areaId].totalScore += rangeScore;
      byArea[areaId].totalExpected += expectedCount;
    });

    // Convert months to years (developmental age)
    byArea[areaId].developmentalAge = areaMonths / 12;
    byArea[areaId].totalPercent = byArea[areaId].totalExpected > 0 
      ? (byArea[areaId].totalScore / byArea[areaId].totalExpected) * 100 
      : 0;

    overallMonths += areaMonths;
    areaCount++;
  });

  const overallDevelopmentalAge = areaCount > 0 ? (overallMonths / areaCount) / 12 : 0;

  // Build summary
  const summary = Object.entries(PORTAGE_AREAS).map(([areaId, area]) => {
    const areaData = byArea[areaId];
    return {
      area: areaId,
      name: area.name,
      totalScore: areaData.totalScore,
      totalExpected: areaData.totalExpected,
      percent: areaData.totalPercent,
      developmentalAge: areaData.developmentalAge
    };
  });

  return {
    byArea,
    overallDevelopmentalAge,
    summary
  };
}

/**
 * Get PORTAGE risk level based on developmental vs chronological age
 * @param {number} developmentalAge - Developmental age in years
 * @param {number} chronologicalAge - Chronological age in years
 * @returns {string} Risk level: 'adequado', 'atenção', or 'atraso'
 */
export function getPortageRiskLevel(developmentalAge, chronologicalAge) {
  const ratio = developmentalAge / chronologicalAge;
  
  if (ratio >= 0.85) {
    return 'adequado';
  } else if (ratio >= 0.70) {
    return 'atenção';
  } else {
    return 'atraso';
  }
}
