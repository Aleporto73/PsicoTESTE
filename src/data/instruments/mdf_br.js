/**
 * Get items for a specific checkpoint
 */
export const getItemsByCheckpoint = (checkpoint) =>
  MDF_BR_ITEMS.filter(item => item.checkpoint_months === checkpoint);

/**
 * Get the appropriate checkpoint for a given age in months
 */
export const getCheckpointForAge = (ageMonths) => {
  const checkpoints = MDF_BR_META.checkpoints;
  // Find the closest checkpoint <= age
  let selected = checkpoints[0];
  for (const cp of checkpoints) {
    if (cp <= ageMonths) selected = cp;
    else break;
  }
  return selected;
};

/**
 * Get weight profile for a checkpoint
 */
export const getWeightProfile = (checkpoint) => {
  const profileCode = MDF_BR_CHECKPOINT_WEIGHTS[checkpoint];
  return MDF_BR_WEIGHT_PROFILES[profileCode] || MDF_BR_WEIGHT_PROFILES.W_EARLY;
};
